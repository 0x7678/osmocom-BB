#include <stdint.h>

#include <layer1/sync.h>


static const uint8_t ubUui[23] = {
	/* dummy lapdm header */
	0x01, 0x03, 0x01,

	/* fill bytes */
	0x2b, 0x2b, 0x2b, 0x2b, 0x2b, 0x2b, 0x2b, 0x2b,
	0x2b, 0x2b, 0x2b, 0x2b, 0x2b, 0x2b, 0x2b, 0x2b,
	0x2b, 0x2b, 0x2b, 0x2b
};


const uint8_t *pu_get_idle_frame(void)
{
	return ubUui;
}
